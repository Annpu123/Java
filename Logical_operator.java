public class Logical_operator {
    public static void main(String[]args){
        // (&&) operator
        int a = 6;
        System.out.println(a >3 && a <10);

         // (||) operator
         int b = 6;
         System.out.println(b >3 || a <10);

          // (!) operator
        int c = 6;
        System.out.println(!(c >3 && a <10));
    }
}
